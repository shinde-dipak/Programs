package Marvellous;

public class Arthimatic
{
    public int Value1;
    public int Value2;

    public Arthimatic(int A , int B)
    {
        Value1 = A;
        Value2 = B;
    }

    public int Addition()
    {
        return Value1 + Value2;
    }
   public int Substraction()
    {
        return Value1 - Value2;
    }
}